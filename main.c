#include <stdio.h>

int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s1[i])
	{
		if (s1[i] == s2[i])
			j++;
		else if (s2[j] == '*')
		{
			while (s2[j] == '*')
				j++;
			i = j - 1;
		}
		i++;
	}
}
