int		ft_atoi(char *str)
{
	int	nbr;
	int neg;
	int i;

	i = 0;
	nbr = 0;
	neg = 1;
	while (str[i] <= 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10 + (str[i] - 48);
		i++;
	}
	return (nbr * neg);
}
